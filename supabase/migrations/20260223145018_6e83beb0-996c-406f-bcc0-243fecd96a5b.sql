-- SALES_EXECUTIVES policies
create policy "se_self_read"
on public.sales_executives
for select
to authenticated
using (
  user_id = auth.uid()
  or public.is_admin(auth.uid())
);

create policy "se_self_update"
on public.sales_executives
for update
to authenticated
using (user_id = auth.uid())
with check (user_id = auth.uid());

create policy "se_admin_insert"
on public.sales_executives
for insert
to authenticated
with check (
  user_id = auth.uid()
  or public.is_admin(auth.uid())
);

create policy "se_admin_update"
on public.sales_executives
for update
to authenticated
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

create policy "se_admin_delete"
on public.sales_executives
for delete
to authenticated
using (public.is_admin(auth.uid()));

