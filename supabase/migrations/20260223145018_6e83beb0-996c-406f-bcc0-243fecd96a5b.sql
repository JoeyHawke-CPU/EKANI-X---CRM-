do $$
begin
  if not exists (select 1 from pg_type where typname = 'user_role') then
    create type public.user_role as enum ('sales', 'admin');
  end if;

  if not exists (select 1 from pg_type where typname = 'lead_status') then
    create type public.lead_status as enum (
      'New Lead',
      'Contacted',
      'Follow-Up',
      'Meeting Scheduled',
      'Nurture',
      'Deal Confirmed – Pending Payment',
      'Paid – In Production',
      'Client Review',
      'Closed – Delivered',
      'Closed Lost'
    );
  end if;
end $$;

-- -----------------------------------------
-- TABLE: sales_executives
-- -----------------------------------------
create table if not exists public.sales_executives (
  user_id uuid primary key references auth.users (id) on delete cascade,
  full_name text not null,
  email text not null unique,
  civil_id text,
  contact_number text,
  role public.user_role not null default 'sales',
  date_joined date not null default current_date,
  commission_default_percentage numeric(6,2) not null default 0,
  active_status boolean not null default true,
  clients_visited_count integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_sales_executives_role on public.sales_executives(role);
create index if not exists idx_sales_executives_active on public.sales_executives(active_status);

-- updated_at trigger function
create or replace function public.set_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end $$;

drop trigger if exists trg_sales_executives_updated_at on public.sales_executives;
create trigger trg_sales_executives_updated_at
before update on public.sales_executives
for each row execute function public.set_updated_at();

-- -----------------------------------------
-- TABLE: leads
-- -----------------------------------------
create table if not exists public.leads (
  lead_id bigint generated by default as identity primary key,
  date_added date not null default current_date,
  sales_exec_id uuid not null references public.sales_executives(user_id) on delete restrict,
  lead_source text,
  client_business_name text not null,
  client_contact_person text,
  phone_number text,
  whatsapp_number text,
  email text,
  governorate text,
  area text,
  business_full_address text,
  domain_status text,
  decision_maker_confirmed boolean not null default false,
  budget_confirmed boolean not null default false,
  timeline_days integer,
  solution_selected text,
  add_ons text,
  quoted_amount_kd numeric(12,3) not null default 0,
  final_agreed_amount_kd numeric(12,3) not null default 0,
  intake_form_completed boolean not null default false,
  payment_link_sent boolean not null default false,
  payment_received boolean not null default false,
  invoice_generated boolean not null default false,
  production_deadline date,
  preview_sent_date date,
  go_live_date date,
  followup_7day_completed boolean not null default false,
  refund_cancellation boolean not null default false,
  escalation_required boolean not null default false,
  remarks text,
  status public.lead_status not null default 'New Lead',
  followup_due_date date,
  commission_percentage numeric(6,2) not null default 0,
  days_to_close integer generated always as (
    case when go_live_date is null then null else (go_live_date - date_added) end
  ) stored,
  commission_eligible boolean generated always as (
    (payment_received = true) and (refund_cancellation = false)
  ) stored,
  commission_amount_kwd numeric(12,3) generated always as (
    case
      when (payment_received = true) and (refund_cancellation = false)
        then round(final_agreed_amount_kd * (commission_percentage / 100.0), 3)
      else 0
    end
  ) stored,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_leads_sales_exec on public.leads(sales_exec_id);
create index if not exists idx_leads_status on public.leads(status);
create index if not exists idx_leads_followup_due on public.leads(followup_due_date);
create index if not exists idx_leads_date_added on public.leads(date_added);

drop trigger if exists trg_leads_updated_at on public.leads;
create trigger trg_leads_updated_at
before update on public.leads
for each row execute function public.set_updated_at();

-- -----------------------------------------
-- SECURITY DEFINER: is_admin()
-- -----------------------------------------
create or replace function public.is_admin(uid uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.sales_executives se
    where se.user_id = uid
      and se.role = 'admin'
      and se.active_status = true
  );
$$;

